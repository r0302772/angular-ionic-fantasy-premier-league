/**
 * Represents a past season in the Fantasy Premier League.
 */
export interface PastSeason {
  /**
   * The name of the past season.
   */
  season_name: string;
  /**
   * The total points earned in the past season.
   */
  total_points: number;
  /**
   * The rank achieved in the past season.
   */
  rank: number;
}

